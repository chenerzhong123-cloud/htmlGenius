#!/usr/bin/env node
// bridge/test/fake-app-server.mjs — 假 Codex App Server,行分隔 JSON-RPC,供 app-server-client / host-runner 单测。
// 行为由 env 驱动:
//   HG_FAKE_MODE: normal | rpc_error | turn_failed | turn_cancelled | timeout | server_request
//   HG_FAKE_WRITE_RESULT=1 时,normal 模式下按 turn/start 的 params.cwd 写出 result.html(模拟 Codex 落盘 candidate)
import process from "node:process";
import fs from "node:fs";
import path from "node:path";

const MODE = process.env.HG_FAKE_MODE || "normal";
const DELAY = Number(process.env.HG_FAKE_DELAY || 20);
const WRITE_RESULT = process.env.HG_FAKE_WRITE_RESULT === "1";
const FAKE_HTML = "<!doctype html><html><head><title>candidate</title></head><body><p>generated by fake codex</p></body></html>";
let nextThr = 1, nextTurn = 1;
let buf = "";

function send(obj) { process.stdout.write(JSON.stringify(obj) + "\n"); }
function reply(id, result) { send({ jsonrpc: "2.0", id, result }); }
function err(id, code, message) { send({ jsonrpc: "2.0", id, error: { code, message } }); }
function notify(method, params) { send({ jsonrpc: "2.0", method, params }); }

function scheduleTurn(turnId, params) {
  setTimeout(() => {
    if (MODE === "turn_failed") { notify("turn/failed", { turn_id: turnId, error: { message: "fake failed" } }); return; }
    if (MODE === "turn_cancelled") { notify("turn/cancelled", { turn_id: turnId }); return; }
    if (MODE === "timeout") { return; /* 永不完成 */ }
    if (MODE === "server_request") { send({ jsonrpc: "2.0", id: 9001, method: "approval/request", params: { turn_id: turnId } }); return; }
    if (WRITE_RESULT && params && params.cwd) {
      try { fs.writeFileSync(path.join(params.cwd, "result.html"), FAKE_HTML); } catch (_) {}
    }
    notify("turn/completed", { turn_id: turnId, output: "fake completion", last_agent_message: "done" });
  }, DELAY);
}

function handle(msg) {
  if (msg.id == null) return; // 客户端发的 notification(如 initialized)忽略
  const m = msg.method;
  if (m === "initialize") {
    if (MODE === "rpc_error") { err(msg.id, -32603, "fake rpc error"); }
    else { reply(msg.id, { serverInfo: { name: "fake-codex", version: "0" } }); }
    return;
  }
  if (m === "thread/start") { reply(msg.id, { thread_id: "thr_fake_" + (nextThr++) }); return; }
  if (m === "thread/resume") { reply(msg.id, { thread_id: (msg.params && msg.params.threadId) || "thr_fake_resumed" }); return; }
  if (m === "turn/start") { const tn = "turn_fake_" + (nextTurn++); reply(msg.id, { turn_id: tn }); scheduleTurn(tn, msg.params); return; }
  err(msg.id, -32601, "fake: unknown method " + m);
}

process.stdin.setEncoding("utf8");
process.stdin.on("data", (c) => {
  buf += c;
  let i;
  while ((i = buf.indexOf("\n")) !== -1) {
    const line = buf.slice(0, i).trim();
    buf = buf.slice(i + 1);
    if (line) { try { handle(JSON.parse(line)); } catch (_) {} }
  }
});
process.stdin.on("end", () => process.exit(0));
